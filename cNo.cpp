#include <iostream>

#include "cNo.h"

using namespace std;

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo::cNo()
{
    topRight = NULL;
    topLeft = NULL;
    botRight = NULL;
    botLeft = NULL;
    q = cQuad();
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo::cNo( cNo* topRight, 
          cNo* topLeft, 
          cNo* botRight, 
          cNo* botLeft)
{
    this->topRight = topRight;
    this->topLeft = topLeft;
    this->botRight = botRight;
    this->botLeft = botLeft;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo::cNo(cPoint p1, cPoint p2)
{
    topRight = NULL;
    topLeft = NULL;
    botRight = NULL;
    botLeft = NULL;
    setQuad(p1,p2);
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo::~cNo()
{

}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setTopRight(cNo* no)
{
    topRight = no;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setTopLeft(cNo* no)
{
    topLeft = no;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setBotRight(cNo* no)
{
    botRight = no;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setBotLeft(cNo* no)
{
    botLeft = no;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo* cNo::getTopRight()
{
    return topRight;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo* cNo::getTopLeft()
{
    return topLeft;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo* cNo::getBotRight()
{
    return botRight;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cNo* cNo::getBotLeft()
{
    return botLeft;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setQuad(cPoint p1, cPoint p2)
{
    q = cQuad(p1,p2);
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cNo::setQuad(cQuad q0)
{
    q = q0;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cQuad cNo::getQuad()
{
    return q;
}